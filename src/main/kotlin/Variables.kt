fun main(args: Array<String>){
    //var type variable can be changed/modified
    var catName = "My cat name is \"fluffy\""
    println(catName)
    //val type variable cannot be change/modified
    val slashes = "This is two type of slashes: \\ and /"
    println(slashes)

    var url = "http://www.google.com"
    println(url)

    var bunny1 = "(\\(\\"
    println(bunny1)
    var bunny2 = "(-.-)"
    println(bunny2)
    var bunny3 = "O_(\")(\")"
    println(bunny3)
}